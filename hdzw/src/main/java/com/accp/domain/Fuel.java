package com.accp.domain;

public class Fuel {
    private Integer kid;

    private String kfuelid;

    private String kfuelname;

    public Integer getKid() {
        return kid;
    }

    public void setKid(Integer kid) {
        this.kid = kid;
    }

    public String getKfuelid() {
        return kfuelid;
    }

    public void setKfuelid(String kfuelid) {
        this.kfuelid = kfuelid;
    }

    public String getKfuelname() {
        return kfuelname;
    }

    public void setKfuelname(String kfuelname) {
        this.kfuelname = kfuelname;
    }
}